package com.example.rest.dto;

public class BandDTO {
	private int id;
	private String name;
	
	public BandDTO() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
